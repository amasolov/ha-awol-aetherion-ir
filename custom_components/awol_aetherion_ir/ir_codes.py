"""AWOL Vision Aetherion Max IR codes learned from the stock remote.

Protocol: NEC-like, 38 kHz carrier.
Codes are stored as base64-encoded Broadlink IR packets, converted to
raw microsecond timings at runtime for the infrared platform.
"""

from __future__ import annotations

import base64
from enum import Enum
