from infrared_protocols import Command

CARRIER_FREQ_HZ = 38_000

_TICK_US = 8192.0 / 269.0


def _broadlink_b64_to_timings(b64: str) -> list[int]:
    """Convert a base64 Broadlink IR packet to signed µs timings."""
    packet = base64.b64decode(b64)
    if len(packet) < 4 or packet[0] != 0x26:
        raise ValueError(f"Invalid Broadlink IR packet (type=0x{packet[0]:02X})")

    length = packet[2] | (packet[3] << 8)
    data = packet[4:4 + length]

    timings: list[int] = []
    i = 0
    is_pulse = True
    while i < len(data):
        if data[i] == 0x00 and i + 2 < len(data):
            ticks = (data[i + 1] << 8) | data[i + 2]
            i += 3
        else:
            ticks = data[i]
            i += 1

        if ticks == 0:
            break

        us = round(ticks * _TICK_US)
        timings.append(us if is_pulse else -us)
        is_pulse = not is_pulse

    return timings


